public class printMath {

    static void  printNumbers(){
        int i = 1;
        for(;i<=10;i++){
            System.out.print(i+" ");
        }
        i=9;
        for(;i>=1;i--){
            System.out.print(i+" ");
        }
    }
    public static void main(String[] args) {
        printNumbers();
    }
}
